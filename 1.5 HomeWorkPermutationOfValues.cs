using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace HWPermutationOfValues
{
    class Program
    {
        static void Main(string[] args)
        {
            string name = "Федоров";
            string lastName = "Даниил";
            string container;

            Console.WriteLine($"Имя: {name} \n" +
                $"Фамилия: {lastName}");

            container = lastName;
            lastName = name;
            name = container;

            Console.WriteLine($" \nИмя: {name} \n" +
                $"Фамилия: {lastName}");
        }
    }
}
